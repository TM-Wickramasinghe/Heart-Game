    private void btndashboardActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btndashboardActionPerformed
        //store the data to public vaiables
        title = lblresult.getText();
        text = result.getText();

        //create method in RankUI to pass the variable data
        Ranking.User1(title);
        Ranking.User(text);

        //display the player's username and score by desending order
        Ranking rank = new Ranking();
        rank.setVisible(true);
        rank.pack();
        rank.setLocationRelativeTo(null);
        rank.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
        try {
            PreparedStatement st;
            String query = "SELECT `players`.`username`, `ranking`.`score` FROM `players` INNER JOIN `ranking` ON `players`.`playerID` = `ranking`.`playerID` ORDER BY `ranking`.`score` DESC ";
            st = MyConnCheck.getConnection().prepareStatement(query);
            ResultSet rs = st.executeQuery();
            rank.tableD.setModel(DbUtils.resultSetToTableModel(rs));

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "There are no players to show!", "ScoreBoard", 1);
        }

        this.dispose();
    }//GEN-LAST:event_btndashboardActionPerformed

